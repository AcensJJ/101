int		ft_count_if(char **tab, int length, int (*f)(char*))
{
	int i;
	int nb;

	i = -1;
	nb = 0;
	while (*tab[++i] != '\0' && i < length)
		if (f(*tab[i]) != 0)
			nb++;
	return (nb);
}
