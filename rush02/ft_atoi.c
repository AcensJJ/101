#include "ft.h"

long		ft_atoi(char *str)
{
	long	i;
	long	signe;
	long	n;

	i = 0;
	n = 0;
	signe = 1;
	while (str[i] == '	' || str[i] == ' ' || str[i] == '\t' || str[i] == '\n'
	|| str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signe = signe * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = (n * 10) + str[i] - '0';
		i++;
	}
	return (n);
}
