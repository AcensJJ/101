int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int c;

	i = 0;
	c = 0;
	while (s1[i] != '\0' && s1[i] == s2[i])
		i++;
	c = s1[i] - s2[i];
	return (c);
}
